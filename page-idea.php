      </header><!-- .entry-header -->

      <div class="entry-content clear" itemprop="text">

        <p>世界中で利用されている WordPress（ワードプレス） では、有料テーマから無料テーマまで、機能もデザインもさまざまなテーマが何千、何万と存在しています。<br> ANDShopでも、公式無料テーマやお客様向けのオリジナルテーマまで、数多くを作成してきました。 </p>

        <p>そんな私たちが新たに開発した「ANDShop（アンドショップ） テーマ」は、お客様や WordPress コミュニティでの多くの方々を通じて感じた「WordPress でのホームページ作りをもっと身近に、もっと簡単に」という想いが込められています。 </p>

        <p>「ANDShop テーマ」開発にあたり、私たちは次の3つをポイントにしました。 </p>



