            <h2>2. プロフェッショナルなチームである</h2>

            <p>2つ目は、プロフェッショナルなチームであるということ。<br> これらの書籍執筆チームは、のべ100サイト以上のウェブサイト制作に関わってきたディレクター・デザイナー・コーダー・エンジニアから成ります。目に見えない部分の技術にもこだわり、SEOにも最適化され、結果を出すことにこだわった作りになっています。</p>

            <p>星の数ほどある他のホームページと同じ舞台で戦うには、これらの目に見えない部分の構造に対してもきちんとした技術を込める必要があると考えます。言い換えれば、制作者の方が手間とコストを削減するためのテーマとしても最適な作りになっています。</p>
