        <p>ANDShopにお越しいただきありがとうございます。<br>より快適にお買い物を楽しんでいただくためのご案内です。<br>ご不明な点がありましたらこちらのご利用ガイドをご活用ください。</p>

        <h2>購入方法について</h2>

        <h3>1. WordPressテーマをご利用になるために、お客様側でご用意いただくもの</h3>

        <ul>
          <li>レンタルサーバーのご契約</li>
          <li>ドメインのご契約</li>
          <li>WordPressのインストール</li>
        </ul>

        <p>WordPressのインストールが難しい場合は、弊社による<a href="/shop/pattern/pack_marugoto">代行サービス</a>もございます。</p>

        <h3>2. 商品をお選びください</h3>

        <p>まずはお客様に合った商品をお選びください。<br>ご不安がありましたら、<a href="/contact/">お問い合わせフォーム</a>からお気軽にお問い合わせください。</p>

        <h3>3. 注文・決済を行います</h3>

        <ol>
          <li>商品詳細の画面で「カートに入れる」ボタンをクリックすると、商品がカートに入り「カート」ページに移動します。</li>
          <li>引き続き商品を追加する場合は、「買い物を続ける」をクリックします。</li>
          <li>「カート」では、お客様が現在ショッピングカートに入れられている商品が一覧で表示されます。</li>
          <li>ユーザー登録されている方はログインを、そのほかの方は請求先情報の詳細にご入力をお願いいたします。（<a href="/buyfaq">購入時のよくあるご質問</a>）</li>
          <li>クーポンコードをお持ちの方は「クーポン」欄に入力し、「クーポンを適用」をクリックします。合計金額にてクーポンが適用されているかご確認ください。</li>
        </ol>

