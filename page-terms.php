        <li>利用契約の変更は、契約者が当社所定の方法により申込み、当社がこれに対し当社所定の方法により承諾の通知を発信したときに成立するものとします。</li>
        <li>当社は、前各項その他利用規約の規定にかかわらず、本サービスの利用申込者及び契約者が次の各号のいずれかに該当する場合には、利用契約又は利用変更契約を締結しないことができます。
