      <h2>株式会社ANDBRO利用規約</h2>
      <p>株式会社ANDBRO（以下「当社」といいます。）は、当社が提供する各種サービスについて、契約者が円滑かつ適正に利用していただくための利用規約（以下「利用規約」といいます。）を以下のとおり定めます。</p>
      <h3>第1章 総則</h3>
      <h4>第1条（利用規約の適用）</h4>
      <ol>
        <li>当社は、利用規約に基づき、本サービスを提供します。</li>
        <li>利用規約と個別の利用契約の規定が異なるときは、個別の利用契約の規定が利用規約に優先して適用されるものとします。</li>
        <li>規約に規定のない本サービスの具体的な内容や料金については、ホームページ上の記載に準拠するものとします。</li>
      </ol>
      <h4>第2条（定義）</h4>
      <p>利用規約においては、次の用語はそれぞれ次の意味で使用します。</p>
      <dl>
        <dt>（1）本サービス</dt>
        <dd>利用規約に基づき当社が契約者に対して提供する各種サービス</dd>
        <dt>（2）契約者</dt>
        <dd>利用規約に基づく利用契約を当社と締結し、本サービスの提供を受ける者</dd>
        <dt>（3）利用契約</dt>
        <dd>利用規約に基づき当社と契約者との間に締結される本サービスの提供に関する契約</dd>
        <dt>（4）利用契約等</dt>
        <dd>利用契約及び利用規約</dd>
        <dt>（5）契約者設備</dt>
        <dd>本サービスの提供を受けるため契約者が設置するコンピュータ、情報端末その他の機器及びソフトウェア</dd>
        <dt>（6）本サービス用設備</dt>
        <dd>本サービスを提供するにあたり、当社が設置するコンピュータ、電気通信設備その他の機器及びソフトウェア</dd>
        <dt>（7）本サービス用設備等</dt>
        <dd>本サービス用設備及び本サービスを提供するために当社が電気通信事業者より借り受ける電気通信回線</dd>
        <dt>（8）消費税等</dt>
        <dd>消費税法及び同法に関連する法令の規定に基づき課税される消費税の額並びに地方税法及び同法に関する法令の規定に基づき課税される地方消費税の額その他契約者が支払に際して負担すべき公租公課</dd>
        <dt>（9）ユーザID</dt>
        <dd>契約者とその他の者を識別するために用いられる符号</dd>
        <dt>（10）パスワード</dt>
        <dd>ユーザIDと組み合わせて、契約者とその他の者を識別するために用いられる符号</dd>
      </dl>
      <h4>第3条（通知）</h4>
      <ol>
        <li>当社から契約者への通知は、利用契約等に特段の定めのない限り、通知内容を電子メール又は当社のホームページに掲載するなど、当社が適当と判断する方法により行います。</li>
        <li>前項の規定に基づき、当社から契約者への通知を電子メールの送信又は当社のホームページへの掲載の方法により行う場合には、契約者に対する当該通知は、それぞれ電子メールの送信又はホームページへの掲載がなされた時点から効力を生じるものとします。</li>
      </ol>
      <h4>第4条（利用規約の変更）</h4>
      <ol>
        <li>当社は、利用規約を随時変更することがあります。なお、この場合には、契約者の利用条件その他利用契約の内容は、変更後の新利用規約を適用するものとします。</li>
        <li>当社は、前項の変更を行う場合は、14日の予告期間をおいて、変更後の新利用規約の内容を契約者に通知するものとします。</li>
      </ol>
      <h4>第5条（権利義務譲渡の禁止）</h4>
      <p>契約者は、あらかじめ当社の書面による承諾がない限り、利用契約上の地位、利用契約に基づく権利又は義務の全部又は一部を他に譲渡してはならないものとします。</p>
