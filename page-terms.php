        <li>当社は、契約者が前項に従った通知を怠ったことにより契約者が通知の不到達その他の事由により損害を被った場合であっても、一切責任を負わないものとします。</li>
        <li>契約者において、相続または合併その他の理由によりその地位の承継があったときは、相続人またはその地位を承継した法人は特段の意思表示がない限り、契約者の地位を承継するものとし、相続人またはその地位を承継した法人は、地位を承継したことを証明する書類を添えて、当社の定める方法により、遅滞なく当社に届出事項の変更をするものとします。</li>
