            <li>利用申込又は利用変更申込に虚偽の記載、誤記があったとき又は記入もれがあったとき</li>
            <li>金銭債務その他利用契約等に基づく債務の履行を怠るおそれがあるとき</li>
            <li>その他当社が不適当と判断したとき</li>
          </ol>
        </li>
      </ol>
